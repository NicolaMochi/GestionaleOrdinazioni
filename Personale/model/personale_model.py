class personale_model:
    def __init__(self, nome, cognome, utente, password, codicePermessi):
        self.nome = nome
        self.cognome = cognome
        self.utente = utente
        self.password = password
        self.codice = codicePermessi

    def get_nome(self):
        return self.nome
    def get_cognome(self):
        return self.nome
    def get_utente(self):
        return self.nome
    def get_password(self):
        return self.nome
    def get_codice(self):
        return self.nome